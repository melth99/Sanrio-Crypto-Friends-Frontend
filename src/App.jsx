async function fetchConvert(formData) {


  try {
    const data = await dataServices.convert(formData.coinFrom, formData.coinTo, formData.fromQuantity);
    console.log(data);
    return data;
  } catch (err) {
    console.error(err);
    throw err; // Re-throw the error
  }
}

